#string functions

story = "once upon a time there was a very beautiful girl"

#1.
# to find length of input
print(len(story)) 

#2.
# to check whether the input ends with 
print(story.endswith("girl"))  

#3.
# to count number of (" ") in the given story
print(story.count("b"))   

#4.
# for the first letter of sentence to be capatalized
print(story.capitalize())  

#5.
# to find the position of (" ") in the given story
print(story.find("upon"))  

#6.
# to replace a word in the given story
print(story.replace("beautiful" , "gorgeous"))   #replce word 
